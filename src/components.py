# Component definitions

# Dynamics

class Position:
    def __init__(self,x,y):
        self.x = x
        self.y = y

class Velocity:
    def __init__(self,vx,vy):
        self.x = vx
        self.y = vy

class Acceleration:
    def __init__(self,ax,ay):
        self.x = ax
        self.y = ay




        
